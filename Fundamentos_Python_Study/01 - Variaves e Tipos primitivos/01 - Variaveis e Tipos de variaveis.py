#Declaração de Variaveis e atribuindo valores de string (str)
produto = 'TV Sansung'
produto2 = "Geladeira Brastemp Frost"
#Output na tela 
print(produto)
print(produto2)
#Output do tipo da variavel  
print(type(produto))
print(type(produto2))

#Declaração de variaveis do tipo float - ponto flutuante (float)
ValorProduto1 = 6.500
ValorProduto2 = 9.270
#Output
print(ValorProduto1)
print(ValorProduto2)
print(type(ValorProduto1))
print(type(ValorProduto2))

#Declarações de variaveis do tipo integer - Tipos inteiros (int)
